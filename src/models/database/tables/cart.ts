import {
    attribute,
    autoGeneratedHashKey,
    table,
} from '@aws/dynamodb-data-mapper-annotations';

/*
 * This class rappresent a cart item in carts table
 */
@table(process.env.CARTS_TABLE)
class Cart {
    @autoGeneratedHashKey()
    id: string;

    @attribute()
    productId: string;

    @attribute()
    customerId: string;

    @attribute()
    quantity: number;

    @attribute()
    discount: number;
}

export default Cart;